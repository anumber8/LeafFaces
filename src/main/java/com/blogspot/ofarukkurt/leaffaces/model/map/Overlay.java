package com.blogspot.ofarukkurt.leaffaces.model.map;

import java.util.Objects;

/**
 *
 * @author Omer Faruk Kurt
 * @Created on date 05/09/2018 08:31:37
 */
public class Overlay implements java.io.Serializable {

    private static final long serialVersionUID = 8091777549151749677L;

    private String id;
    private Object data;

    public Overlay() {
    }

    public Overlay(Object data) {
        this.data = data;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 59 * hash + Objects.hashCode(this.id);
        hash = 59 * hash + Objects.hashCode(this.data);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Overlay other = (Overlay) obj;
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        if (!Objects.equals(this.data, other.data)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Overlay{" + "id=" + id + ", data=" + data + '}';
    }

}
